import turtle
tina = turtle.Turtle()
tina.shape('turtle')
turtle.colormode(255)




















tina.up()
tina.forward(150)
tina.down()

for i in range(0, 3):
    tina.left(120)
    tina.forward(300)

tina.up()
tina.left(120)
tina.forward(100)
tina.right(60)
tina.forward(100)
tina.down()

for i in range(0, 3):
    tina.left(120)
    tina.forward(300)

turtle.done()
